#include "lem_in.h"

_Bool	is_tube(char *s)
{
	if (!(s && *s && ft_strchr(s, '-')))
		return (0);
	return (1);
}

_Bool	check_tube(char *s)
{
	if (!(s && is_tube(s)))
		return (0);
	return (1);
}
